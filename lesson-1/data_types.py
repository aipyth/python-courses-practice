# Ми можемо записувати у змінну числові значення. Це зручно для обчислень.
a = 1
b = 3
# Можемо вивести значення змінної ось так
print(a)
# Вивести значення виразу можемо також через функцію print
print(a + b)
# У print можемо передати не тільки один аргумент, а й скільки захочемо. Агрументи розділяємо комами
print("У змінній b маємо ", b)


